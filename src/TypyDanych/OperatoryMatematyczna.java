package TypyDanych;

public class OperatoryMatematyczna {
    public static void main(String[] args) {

        System.out.println(10 -10);
        System.out.println(10+10);
        System.out.println(10*10);
        System.out.println(10/10);

        double a = 5;
        double b = 10;

        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);

        //OperatorModulo % - podaje reszta z dzielenia

        System.out.println(a%b);

    }
}
